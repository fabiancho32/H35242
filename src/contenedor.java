
public class contenedor {

	String nombre;
	String tipo;
	Token tokenvalor;
	Boolean asignacion;
	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * @return the tipo
	 */
	public String getTipo() {
		return tipo;
	}
	/**
	 * @param tipo the tipo to set
	 */
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	/**
	 * @return the tokenvalor
	 */
	public Token getTokenvalor() {
		return tokenvalor;
	}
	/**
	 * @param tokenvalor the tokenvalor to set
	 */
	public void setTokenvalor(Token tokenvalor) {
		this.tokenvalor = tokenvalor;
	}
	/**
	 * @return the asignacion
	 */
	public Boolean getAsignacion() {
		return asignacion;
	}
	/**
	 * @param asignacion the asignacion to set
	 */
	public void setAsignacion(Boolean asignacion) {
		this.asignacion = asignacion;
	}

	public contenedor(){
		
	}
	
}
